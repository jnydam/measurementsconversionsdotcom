// Meters Per Second --  Miles Per Hour Conversions

// approximate
export const convertMetersPerSecondToMilesPerHour = (
  metersPerSecond: number
) => {
  return metersPerSecond * 2.237;
};

// approximate
export const convertMilesPerHourToMetersPerSecond = (milesPerHour: number) => {
  return milesPerHour / 2.237;
};
